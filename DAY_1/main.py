# Printing text
print("1. Hello World!\n2. Hello World!\n3. Hello World!")

# Strings concatenation
print("4. Hello" + " " + "World" + " " + "!")

# User input
print("Hello, " + input("What is your name?") + "!")

# Variables
name = input("What is your real name?")
print("Hello, " + name + "! You name is now stored in a variable =)")

# Determining the length of the user input
username = input("What is your name?")
username_length = len(username)
print("Hello, " + username + ". The length of your name is " + str(username_length))
